package entity;

public class Barang {
    int id;
    String idBarang;
    String beratBarang;
    String hargaBarang;
    String jumlahBarang;
    String namaBarang;
    String jenisBarang;
    String merkBarang;


    public String getIdBarang() {
        return idBarang;
    }

    public void setIdBarang(String id) {
        this.idBarang = id;
    }

    public String getBeratBarang() {
        return beratBarang;
    }

    public void setBeratBarang(String beratBarang) {
        this.beratBarang = beratBarang;
    }

    public String getHargaBarang() {
        return hargaBarang;
    }

    public void setHargaBarang(String hargaBarang) {
        this.hargaBarang = hargaBarang;
    }

    public String getJumlahBarang() {
        return jumlahBarang;
    }

    public String getMerkBarang() {
        return merkBarang;
    }

    public void setMerkBarang(String merkBarang) {
        this.merkBarang = merkBarang;
    }

    public void setJumlahBarang(String jumlahBarang) {
        this.jumlahBarang = jumlahBarang;
    }

    public String getNamaBarang() {
        return namaBarang;
    }

    public void setNamaBarang(String namaBarang) {
        this.namaBarang = namaBarang;
    }

    public String getJenisBarang() {
        return jenisBarang;
    }

    public void setJenisBarang(String jenisBarang) {
        this.jenisBarang = jenisBarang;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
